    private void btnAddCityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCityActionPerformed

        int f=0;
        String name = txtName.getText();
        if (name.matches("^([A-Za-z]+)(\\s[A-Za-z]+)*\\s?$")) {
            name = name.trim();
            for(String s:networkstring){
                if(s.equalsIgnoreCase(name)){
                    JOptionPane.showMessageDialog(null, "The city already exists in the network");
                    f=1;
                    break;
                }
            }
            if(f==0){
            networkstring.add(name);
            Network network = system.createAndAddNetwork();
            network.setName(name);
            network.setState(txtState.getText());
            network.setZip(txtZipCode.getText());
            intiNetworkTable();
            txtName.setText("");
            txtState.setText("");
            txtZipCode.setText("");
            }    
        } else {
            JOptionPane.showMessageDialog(null,"No special character allowed");
        }
    }//GEN-LAST:event_btnAddCityActionPerformed

